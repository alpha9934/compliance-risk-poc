class TestSHAPExplainer:

    @pytest.fixture(scope="class")
    def explainer(self):
        """Create a real SHAPExplainer using the trained model."""
        if _BOOSTER is None:
            pytest.skip("XGBoost model not found — run notebooks/02_model_training.py first")
        return SHAPExplainer(_BOOSTER, FEATURE_NAMES, _MODEL_VERSION)

    @pytest.fixture(scope="class")
    def sample_input(self):
        """A high-risk feature vector as numpy array."""
        fv = make_fv(
            amount=95000.0,
            jurisdiction_destination="AE",
            timestamp=datetime(2026, 6, 13, 2, 0, 0, tzinfo=timezone.utc),
        )
        return np.array([fv.to_model_input()])

    def test_returns_shap_explanation_object(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        assert isinstance(result, SHAPExplanation)

    def test_top_features_count_default_5(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        assert len(result.top_features) == 5

    def test_top_features_count_custom(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score, top_n=3)
        assert len(result.top_features) == 3

    def test_features_sorted_by_abs_contribution(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        abs_contribs = [f.abs_contribution for f in result.top_features]
        assert abs_contribs == sorted(abs_contribs, reverse=True)

    def test_feature_names_are_valid(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        for f in result.top_features:
            assert f.feature in FEATURE_NAMES

    def test_predicted_score_stored_correctly(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        assert abs(result.predicted_score - round(predicted_score, 4)) < 1e-6

    def test_base_value_is_float(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        assert isinstance(result.base_value, float)

    def test_model_version_matches(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        assert result.model_version == _MODEL_VERSION

    def test_abs_contribution_equals_abs_of_contribution(self, explainer, sample_input):
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        for f in result.top_features:
            assert abs(round(f.contribution, 4)) == f.abs_contribution

    def test_high_risk_features_have_positive_contributions(self, explainer, sample_input):
        """For a genuinely high-risk transaction, top features should push score UP."""
        predicted_score = float(_BOOSTER.predict(xgb.DMatrix(sample_input, feature_names=FEATURE_NAMES))[0])
        result = explainer.explain(sample_input, predicted_score)
        # At least the top feature should be positive for a high-risk case
        assert result.top_features[0].contribution > 0


