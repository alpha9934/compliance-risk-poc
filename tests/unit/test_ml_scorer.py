    def test_model_version_set(self):
        fv     = make_fv()
        result = score_transaction(fv)
        assert result.model_version != ""

    def test_high_risk_transaction_scores_high(self):
        """A textbook high-risk transaction should score HIGH."""
        event = make_event(
            amount=200000.0,
            jurisdiction_destination="RU",
            product_type="WIRE_TRANSFER",
            timestamp=datetime(2026, 6, 13, 3, 0, 0, tzinfo=timezone.utc),
        )
        store = make_store(avg_amount=3000.0, risk_rating=4, prior_alerts=2)
        fv    = build_feature_vector(event, store)
        result = score_transaction(fv, product_type="WIRE_TRANSFER", jurisdiction_destination="RU")
        assert result.risk_class == RiskClass.HIGH
        assert result.risk_score > 0.7

    def test_low_risk_transaction_scores_low(self):
        """A clean domestic transaction should score LOW."""
        event = make_event(
            amount=1500.0,
            jurisdiction_origin="US",
            jurisdiction_destination="US",
            product_type="ACH",
        )
        store = make_store(avg_amount=2000.0, risk_rating=1, prior_alerts=0)
        fv    = build_feature_vector(event, store)
        result = score_transaction(fv, product_type="ACH", jurisdiction_destination="US")
        assert result.risk_class == RiskClass.LOW
        assert result.risk_score < 0.45

    def test_blacklisted_destination_always_high(self):
        """FATF blacklisted destination (IR/KP) must produce HIGH risk."""
        event = make_event(
            amount=50000.0,
            jurisdiction_destination="IR",
            product_type="SWIFT_TRANSFER",
        )
        store = make_store(avg_amount=5000.0)
        fv    = build_feature_vector(event, store)
        result = score_transaction(fv, product_type="SWIFT_TRANSFER", jurisdiction_destination="IR")
        assert result.risk_class == RiskClass.HIGH

    def test_product_jurisdiction_threshold_respected(self):
        """Wire to AE should use tighter thresholds than Wire to US."""
        event_ae = make_event(
            amount=30000.0,
            jurisdiction_destination="AE",
            product_type="WIRE_TRANSFER",
        )
        event_us = make_event(
            amount=30000.0,
            jurisdiction_destination="US",
            product_type="WIRE_TRANSFER",
        )
        store = make_store(avg_amount=5000.0)
        fv_ae = build_feature_vector(event_ae, store)
        fv_us = build_feature_vector(event_us, store)

        result_ae = score_transaction(fv_ae, "WIRE_TRANSFER", "AE")
        result_us = score_transaction(fv_us, "WIRE_TRANSFER", "US")

        # AE destination should produce higher or equal risk class
        risk_order = {RiskClass.LOW: 0, RiskClass.MEDIUM: 1, RiskClass.HIGH: 2}
        assert risk_order[result_ae.risk_class] >= risk_order[result_us.risk_class]

    def test_scorer_mode_is_xgboost_when_model_loaded(self):
        if _BOOSTER is None:
            pytest.skip("Model not found")
        fv     = make_fv()
        result = score_transaction(fv)
        assert result.scorer_mode == "xgboost"

    def test_feature_names_length_matches_to_model_input(self):
        fv = make_fv()
        assert len(FEATURE_NAMES) == len(fv.to_model_input())

    def test_consecutive_calls_are_deterministic(self):
        """Same input must always produce same output — model is stateless."""
        fv      = make_fv(amount=75000.0, jurisdiction_destination="AE")
        result1 = score_transaction(fv)
        result2 = score_transaction(fv)
        assert result1.risk_score == result2.risk_score
        assert result1.risk_class == result2.risk_class

    def test_amount_zscore_appears_in_shap_top5_for_outlier(self):
        """When amount is a huge outlier, zscore should appear in top SHAP features."""
        event = make_event(amount=500000.0)
        store = make_store(avg_amount=2000.0)
        fv    = build_feature_vector(event, store)
        result = score_transaction(fv)
        feature_names_in_top5 = [f.feature for f in result.shap_explanation.top_features]
        assert any("zscore" in n or "outlier" in n or "risk" in n for n in feature_names_in_top5)


