class TestMLScorerIntegration:

    def test_full_pipeline_high_risk_transaction(self):
        event = TransactionEvent(
            transaction_id="TXN-INTEG-HIGH",
            customer_id="CUST-INTEG-01",
            amount=150000.0,
            currency="USD",
            channel="WIRE",
            origin_account="ACC-US-111",
            destination_account="ACC-RU-999",
            jurisdiction_origin="US",
            jurisdiction_destination="RU",
            product_type="WIRE_TRANSFER",
            timestamp=datetime(2026, 6, 13, 1, 30, 0, tzinfo=timezone.utc),
        )
        store = FeatureStoreClient()
        store.seed_customer("CUST-INTEG-01",
            avg_amount=4000.0, risk_rating=4,
            prior_alerts=2, account_age_days=90,
        )
        fv     = build_feature_vector(event, store)
        result = score_transaction(fv, "WIRE_TRANSFER", "RU")

        assert result.risk_class == RiskClass.HIGH
        assert result.risk_score > 0.5
        assert len(result.shap_explanation.top_features) == 5
        assert result.transaction_id == "TXN-INTEG-HIGH"
        assert result.scorer_mode in ("xgboost", "heuristic")

    def test_full_pipeline_low_risk_transaction(self):
        event = TransactionEvent(
            transaction_id="TXN-INTEG-LOW",
            customer_id="CUST-INTEG-02",
            amount=800.0,
            currency="USD",
            channel="ACH",
            origin_account="ACC-US-222",
            destination_account="ACC-US-333",
            jurisdiction_origin="US",
            jurisdiction_destination="US",
            product_type="ACH_PAYMENT",
            timestamp=datetime(2026, 6, 13, 11, 0, 0, tzinfo=timezone.utc),
        )
        store = FeatureStoreClient()
        store.seed_customer("CUST-INTEG-02",
            avg_amount=1000.0, risk_rating=1,
            prior_alerts=0, account_age_days=730,
        )
        # Seed some normal transaction history
        for i in range(5):
            store.append_customer_txn("CUST-INTEG-02", {
                "transaction_id": f"TXN-HIST-{i}",
                "amount": 950.0, "currency": "USD", "channel": "ACH",
                "destination_account": "ACC-US-333",
                "jurisdiction_destination": "US",
                "timestamp": "2026-06-10T10:00:00+00:00",
            })

        fv     = build_feature_vector(event, store)
        result = score_transaction(fv, "ACH_PAYMENT", "US")

        assert result.risk_class == RiskClass.LOW
        assert result.risk_score < 0.50

    def test_scorer_output_serialises_to_json(self):
        """ScorerOutput must be JSON-serialisable for API response."""
        fv     = make_fv()
        result = score_transaction(fv)
        json_str = result.model_dump_json()
        assert "risk_score" in json_str
        assert "risk_class" in json_str
        assert "top_features" in json_str

    def test_shap_feature_values_match_feature_vector(self):
        """SHAP feature values must match what was actually in the FeatureVector."""
        event = make_event(amount=50000.0, jurisdiction_destination="AE")
        store = make_store(avg_amount=5000.0)
        fv    = build_feature_vector(event, store)
        result = score_transaction(fv)

        fv_dict = fv.model_dump()
        for feature_contrib in result.shap_explanation.top_features:
            name = feature_contrib.feature
            if name in fv_dict:
                expected = round(float(fv_dict[name]), 4)
                assert feature_contrib.value == expected, (
                    f"SHAP value mismatch for feature '{name}': "
                    f"expected {expected}, got {feature_contrib.value}"
                )
