# ── Shared helpers ─────────────────────────────────────────────────────────

def make_event(**overrides) -> TransactionEvent:
    defaults = dict(
        transaction_id="TXN-ML-001",
        customer_id="CUST-ML-42",
        amount=5000.0,
        currency="USD",
        channel="WIRE",
        origin_account="ACC-US-001",
        destination_account="ACC-US-002",
        jurisdiction_origin="US",
        jurisdiction_destination="US",
        product_type="WIRE_TRANSFER",
        timestamp=datetime(2026, 6, 13, 10, 0, 0, tzinfo=timezone.utc),
    )
    defaults.update(overrides)
    return TransactionEvent(**defaults)


def make_store(**kwargs) -> FeatureStoreClient:
    store = FeatureStoreClient()
    store.seed_customer(
        "CUST-ML-42",
        avg_amount=kwargs.get("avg_amount", 5000.0),
        risk_rating=kwargs.get("risk_rating", 2),
        prior_alerts=kwargs.get("prior_alerts", 0),
        account_age_days=kwargs.get("account_age_days", 365),
    )
    return store


def make_fv(**event_overrides) -> FeatureVector:
    event = make_event(**event_overrides)
    store = make_store()
    return build_feature_vector(event, store)


