class TestHeuristicScorer:
    """Tests for the rule-based fallback scorer (no model needed)."""

    def _make_fv(self, **kwargs) -> FeatureVector:
        defaults = dict(
            transaction_id="TXN-HEUR-001",
            customer_id="CUST-HEUR-42",
            count_24h=0, count_7d=0, sum_24h=0.0, sum_7d=0.0,
            unique_dest_7d=1, amount_zscore=0.0, amount_vs_avg_ratio=1.0,
            is_amount_outlier=False, destination_risk_score=1, risk_score_delta=0,
            is_high_risk_dest=False, is_fatf_greylist=False, is_fatf_blacklist=False,
            is_cross_border=False, is_new_beneficiary=False, dest_account_age_days=365,
            customer_risk_rating=2, prior_alert_count=0, is_high_risk_customer=False,
            has_prior_alerts=False, channel_switch_flag=False, time_of_day_risk=0.1,
            is_round_amount=False, is_structuring_pattern=False,
        )
        defaults.update(kwargs)
        return FeatureVector(**defaults)

    def test_clean_transaction_scores_low(self):
        fv    = self._make_fv()
        score = _heuristic_score(fv)
        assert score < 0.30

    def test_fatf_blacklist_adds_high_score(self):
        fv_clean = self._make_fv()
        fv_black = self._make_fv(is_fatf_blacklist=True)
        assert _heuristic_score(fv_black) > _heuristic_score(fv_clean) + 0.15

    def test_fatf_greylist_adds_moderate_score(self):
        fv_clean = self._make_fv()
        fv_grey  = self._make_fv(is_fatf_greylist=True)
        assert _heuristic_score(fv_grey) > _heuristic_score(fv_clean)

    def test_structuring_pattern_adds_significant_score(self):
        fv_clean   = self._make_fv()
        fv_struct  = self._make_fv(is_structuring_pattern=True)
        assert _heuristic_score(fv_struct) > _heuristic_score(fv_clean) + 0.05

    def test_multiple_flags_cumulate(self):
        fv_single = self._make_fv(is_fatf_greylist=True)
        fv_multi  = self._make_fv(
            is_fatf_greylist=True,
            is_high_risk_dest=True,
            is_amount_outlier=True,
            is_new_beneficiary=True,
        )
        assert _heuristic_score(fv_multi) > _heuristic_score(fv_single)

    def test_score_never_exceeds_0_99(self):
        fv = self._make_fv(
            is_fatf_blacklist=True, is_fatf_greylist=True,
            is_high_risk_dest=True, is_amount_outlier=True,
            is_structuring_pattern=True, is_new_beneficiary=True,
            is_high_risk_customer=True, has_prior_alerts=True,
            amount_zscore=10.0, count_24h=20, customer_risk_rating=5,
        )
        assert _heuristic_score(fv) <= 0.99

    def test_score_never_negative(self):
        fv = self._make_fv()
        assert _heuristic_score(fv) >= 0.0

    def test_heuristic_shap_returns_5_features(self):
        fv    = self._make_fv(is_fatf_greylist=True, amount_zscore=3.0)
        score = _heuristic_score(fv)
        shap  = _heuristic_shap(fv, score)
        assert len(shap.top_features) == 5

    def test_heuristic_shap_features_sorted_by_abs_contribution(self):
        fv    = self._make_fv(is_fatf_greylist=True, is_high_risk_dest=True)
        score = _heuristic_score(fv)
        shap  = _heuristic_shap(fv, score)
        abs_vals = [f.abs_contribution for f in shap.top_features]
        assert abs_vals == sorted(abs_vals, reverse=True)

    def test_heuristic_shap_predicted_score_matches(self):
        fv    = self._make_fv()
        score = _heuristic_score(fv)
        shap  = _heuristic_shap(fv, score)
        assert shap.predicted_score == score


