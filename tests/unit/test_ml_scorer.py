class TestThresholdPolicy:

    def test_high_score_returns_high_class(self):
        assert classify_risk(0.90) == RiskClass.HIGH

    def test_medium_score_returns_medium_class(self):
        assert classify_risk(0.55) == RiskClass.MEDIUM

    def test_low_score_returns_low_class(self):
        assert classify_risk(0.20) == RiskClass.LOW

    def test_exact_high_boundary(self):
        # Score exactly at high threshold should return HIGH
        thresholds = get_thresholds("DEFAULT", "DEFAULT")
        assert classify_risk(thresholds["high"]) == RiskClass.HIGH

    def test_just_below_high_boundary(self):
        thresholds = get_thresholds("DEFAULT", "DEFAULT")
        score = thresholds["high"] - 0.001
        assert classify_risk(score) == RiskClass.MEDIUM

    def test_exact_medium_boundary(self):
        thresholds = get_thresholds("DEFAULT", "DEFAULT")
        assert classify_risk(thresholds["medium"]) == RiskClass.MEDIUM

    def test_just_below_medium_boundary(self):
        thresholds = get_thresholds("DEFAULT", "DEFAULT")
        score = thresholds["medium"] - 0.001
        assert classify_risk(score) == RiskClass.LOW

    def test_wire_transfer_to_ae_has_lower_threshold(self):
        # AE-specific threshold should be lower than DEFAULT
        ae_thresholds      = get_thresholds("WIRE_TRANSFER", "AE")
        default_thresholds = get_thresholds("WIRE_TRANSFER", "DEFAULT")
        assert ae_thresholds["high"] < default_thresholds["high"]

    def test_unknown_product_falls_back_to_default(self):
        # Unknown product should not crash — uses DEFAULT
        result = classify_risk(0.80, product_type="CRYPTO_TRANSFER")
        assert result in (RiskClass.HIGH, RiskClass.MEDIUM, RiskClass.LOW)

    def test_unknown_jurisdiction_falls_back_to_product_default(self):
        result = classify_risk(0.80, jurisdiction_destination="ZZ")
        assert result in (RiskClass.HIGH, RiskClass.MEDIUM, RiskClass.LOW)

    def test_ach_has_higher_threshold_than_swift(self):
        # ACH is lower risk channel — higher thresholds before flagging
        ach_thresh  = get_thresholds("ACH",   "DEFAULT")
        swift_thresh = get_thresholds("SWIFT", "DEFAULT")
        assert ach_thresh["high"] > swift_thresh["high"]

    def test_score_zero_is_low(self):
        assert classify_risk(0.0) == RiskClass.LOW

    def test_score_one_is_high(self):
        assert classify_risk(1.0) == RiskClass.HIGH

    def test_get_thresholds_returns_high_and_medium_keys(self):
        t = get_thresholds()
        assert "high" in t
        assert "medium" in t

    def test_medium_threshold_always_below_high(self):
        for product in ["DEFAULT", "WIRE_TRANSFER", "ACH", "SWIFT"]:
            t = get_thresholds(product)
            assert t["medium"] < t["high"], f"medium >= high for product={product}"


